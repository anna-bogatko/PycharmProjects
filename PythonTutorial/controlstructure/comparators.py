"""
== --> Value Equality
!= --> Not equal to
< --> Less than
> --> Greater than
<= --> Less than or equal to
>= --> Greater than or equal to
"""

bool_one = 10 == 10
print(bool_one)

bool_two = 10 == 11
print(bool_two)

not_equal = 10 != 19
print(not_equal)

less_than = 10 < 100
print(less_than)

greater_than = 10 > 9
print(greater_than)

lessthan_equal = 10 <= 11
print(lessthan_equal)

greaterthan_equal = 10 >= 11
print(greaterthan_equal)

